def seed_extra():
    db = SessionLocal()
    try:
        for t_data in TERMS:
            existing = db.query(TermDictionary).filter(TermDictionary.term == t_data["term"]).first()
            if existing:
                for key, val in t_data.items():
                    setattr(existing, key, val)
            else:
                db.add(TermDictionary(**t_data))
        db.commit()
        print(f"[OK] Added/Updated {len(TERMS)} terms.")
    finally:
        db.close()

if __name__ == "__main__":
    seed_extra()
